    public void atualizaTabela () {
        listVendas.clear(); // limpar tabela
        listVendas.addAll(daoVenda.getLista()); //pegar a lista e adicionar todos os elementos da lista pegando ds listcidade da classe Dados
        int linha = listVendas.size() -1;  // para ver se vai ter conteudo na lista
        if(linha >=0 ) { // verificando se tem conteudo na lista
            tblVenda.setRowSelectionInterval(linha, linha); //pegar o tamanho da lista e jogar
            tblVenda.scrollRectToVisible(tblVenda.getCellRect(linha, linha, true)); // criar barra de rolagem
        }
        
    }
    
    private void trataEdicao (boolean editando) {
        btnCancelar.setEnabled(editando); // manda true quando for editar
        btnSalvar.setEnabled(editando); // true
        btnEditar.setEnabled(!editando); // false, desabilita o botão editar
        
        int linha = listVendas.size() -1;
        if(linha <0) { // sea linha menor que 0 por que nao tem nenhum registro, nenhum elemento na lista
            btnExcluir.setEnabled(false); // desativa o botao excluir
            txtCodigo.setText(""); // codigo nao recebe valor
            txtDataVenda.setText("");
        } else { //se nao ele habilita
            btnExcluir.setEnabled(!editando); // ele fica verdadeiro
        }
        
        // desabilita os botoes ao clicar no novo novo, anterior e ultimo
        btnAnterior.setEnabled(!editando);
        btnFechar.setEnabled(!editando);
        btnUltimo.setEnabled(!editando);
        btnPrimeiro.setEnabled(!editando);
        btnProximo.setEnabled(!editando);
        cbxCliente.setEnabled(editando);
        txtDataVenda.setEditable(editando);
        cbxFuncionario.setEditable(!editando);      
    }
    
    public boolean validaCampo () {
        if(!(cbxCliente.getSelectedIndex() >=0 )) { // verificação enquanto ele nao digitar nada ele nao retorna true
            JOptionPane.showMessageDialog(null, "Informe o nome do Cliente");
            cbxCliente.requestFocus();
            return false;
        }
        
             
        if(!(cbxFuncionario.getSelectedIndex()>=0 )) {
            JOptionPane.showMessageDialog(null, "Informe o Funcionário");
            cbxFuncionario.requestFocus();
            return false;
        }
        
        if(!(txtDataVenda.getText().length() > 0)) {
            JOptionPane.showMessageDialog(null, "Informe a Data de venda");
            txtDataVenda.requestFocus();
            return false;
        }
        
        return true;        
    }

