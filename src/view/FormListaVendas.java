    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        int opcao = JOptionPane.showOptionDialog(null,"Confirmar Exclusão?", "Pergunta", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, new String[]{"Sim", "Não"}, "Sim"); // uma janela de pergunta ao usuario, que tem 2 opção cira o vetor com 2 opções com 0 e 1 , se cliecar em sim é 0 e nao = 1
        if (opcao == 0) { // se clicar em sim
            int linhaSelecionada = tblVenda.getSelectedRow(); // pegar linha selecionada
            Venda objFuncionario = listVendas.get(linhaSelecionada); // obtem o objeto que vai remover
            daoVenda.remover(objFuncionario); // atraves do dao vai remover
            atualizaTabela(); // atualiza tabela
            trataEdicao(false); // inicializa novamente
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnProximoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProximoActionPerformed
        // TODO add your handling code here:
        int linha = tblVenda.getSelectedRow(); // pega intervalo da linha
        if(linha +1 <= tblVenda.getRowCount() -1) { // rowCount conta quantos registro tem na tabela, se tiver na ultima linha nao vai mais pra frente
            linha++; // decrementa
        }
        tblVenda.setRowSelectionInterval(linha, linha); // seleicona nossa linha
        tblVenda.scrollRectToVisible(tblVenda.getCellRect(linha, 0, true)); // mostra a linha selecionada
    }//GEN-LAST:event_btnProximoActionPerformed

