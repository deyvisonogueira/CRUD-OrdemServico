    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        if (validaCampo()) { // validação de campos na hora de salvar
        int linhaSelecionada = tblVenda.getSelectedRow(); // pegar a linha que ta selecionada
        Venda objFuncionario =listVendas.get(linhaSelecionada); //crio o objeto cliente e joga na linha
        daoVenda.salvar(objFuncionario); //pega o objeto no estado que ta e salva 
        atualizaTabela(); // atualiza tabela mostrando objeto criado
        trataEdicao(false); // apos clicar em salvar manda um false para desabilitar o botão
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

